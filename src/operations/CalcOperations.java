package operations;

public class CalcOperations {

    public static double calcSum(double a, double b) {
        return a + b;
    }

    public static double calcSubtact(double a, double b) {
        return a - b;
    }

    public static double calcMultuply(double a, double b) {
        return a * b;
    }

    public static double calcDivide(double a, double b) {
        return a / b;
    }

}
